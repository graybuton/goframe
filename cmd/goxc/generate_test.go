func TestPartialPackageGenerationRejectsStaleSiblingAcrossOutputModes(t *testing.T) {
	tests := []struct {
		name  string
		setup func(*testing.T, string) (func(string) generateOptions, string)
	}{
		{
			name: "default hidden",
			setup: func(t *testing.T, root string) (func(string) generateOptions, string) {
				t.Helper()
				layout, err := newBuildLayout(layoutOptions{appDir: root})
				if err != nil {
					t.Fatal(err)
				}
				return func(source string) generateOptions {
					return generateOptions{path: source}
				}, layout.GenDir
			},
		},
		{
			name: "explicit output",
			setup: func(t *testing.T, _ string) (func(string) generateOptions, string) {
				t.Helper()
				outputRoot := t.TempDir()
				return func(source string) generateOptions {
					return generateOptions{path: source, outDir: outputRoot}
				}, outputRoot
			},
		},
	}

	for _, test := range tests {
		t.Run(test.name, func(t *testing.T) {
			root := newPackageIdentifierFixture(t)
			existingSource := filepath.Join(root, "view_A.gox")
			requestedSource := filepath.Join(root, "view.gox")
			options, outputRoot := test.setup(t, root)
			existingOutput := filepath.Join(outputRoot, "view_A.gox.go")
			requestedOutput := filepath.Join(outputRoot, "view.gox.go")
			writeTestFile(t, root, "view_A.gox", packageIdentifierGOXSource("Other", "B"))
			if _, err := runGeneratePathForTest(t, options(existingSource)); err != nil {
				t.Fatalf("initial generatePath() error: %v", err)
			}
			existingBefore, err := os.ReadFile(existingOutput)
			if err != nil {
				t.Fatal(err)
			}
			unrelated := filepath.Join(outputRoot, "keep.txt")
			if err := os.WriteFile(unrelated, []byte("keep"), 0o644); err != nil {
				t.Fatal(err)
			}

			writeTestFile(t, root, "view.gox", packageIdentifierGOXSource("View", "A_B"))
			_, generationErr := runGeneratePathForTest(t, options(requestedSource))
			assertPartialGenerationStaleError(
				t,
				generationErr,
				requestedSource,
				existingSource,
				existingOutput,
				root,
			)
			if _, err := os.Stat(requestedOutput); !os.IsNotExist(err) {
				t.Fatalf("requested output exists after stale guard: %v", err)
			}
			assertFileBytes(t, existingOutput, existingBefore)
			assertFileBytes(t, unrelated, []byte("keep"))
		})
	}
}

