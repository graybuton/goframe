func TestPartialPackageGenerationRejectsUnverifiableSiblingOutput(t *testing.T) {
	t.Run("unmanaged", func(t *testing.T) {
		_, requestedSource, requestedOutput, siblingSource, siblingOutput := newPartialOutputSafetyFixture(t)
		content := []byte("package app\n\nvar authored = true\n")
		if err := os.WriteFile(siblingOutput, content, 0o644); err != nil {
			t.Fatal(err)
		}

		_, err := runGeneratePathForTest(t, generateOptions{
			path:   requestedSource,
			outDir: filepath.Dir(siblingOutput),
		})
		if err == nil {
			t.Fatal("partial generation accepted an unmanaged sibling output")
		}
		for _, want := range []string{
			requestedSource,
			siblingSource,
			siblingOutput,
			"not managed by goxc",
		} {
			if !strings.Contains(err.Error(), want) {
				t.Fatalf("error %q does not contain %q", err, want)
			}
		}
		assertFileBytes(t, siblingOutput, content)
		if _, statErr := os.Stat(requestedOutput); !os.IsNotExist(statErr) {
			t.Fatalf("requested output exists after ownership failure: %v", statErr)
		}
	})

	t.Run("symlink", func(t *testing.T) {
		requireSymlinkSupport(t)
		_, requestedSource, requestedOutput, siblingSource, siblingOutput := newPartialOutputSafetyFixture(t)
		external := filepath.Join(t.TempDir(), "external.gox.go")
		externalContent := []byte(generatedGOXFileHeader + "package outside\n")
		if err := os.WriteFile(external, externalContent, 0o644); err != nil {
			t.Fatal(err)
		}
		if err := os.Symlink(external, siblingOutput); err != nil {
			t.Fatal(err)
		}

		_, err := runGeneratePathForTest(t, generateOptions{
			path:   requestedSource,
			outDir: filepath.Dir(siblingOutput),
		})
		if err == nil {
			t.Fatal("partial generation accepted a symlink sibling output")
		}
		for _, want := range []string{
			requestedSource,
			siblingSource,
			siblingOutput,
			"cannot be verified",
			"symlink",
		} {
			if !strings.Contains(err.Error(), want) {
				t.Fatalf("error %q does not contain %q", err, want)
			}
		}
		assertFileBytes(t, external, externalContent)
		if _, statErr := os.Stat(requestedOutput); !os.IsNotExist(statErr) {
			t.Fatalf("requested output exists after symlink failure: %v", statErr)
		}
	})

	t.Run("irregular", func(t *testing.T) {
		_, requestedSource, requestedOutput, siblingSource, siblingOutput := newPartialOutputSafetyFixture(t)
		if err := os.Mkdir(siblingOutput, 0o755); err != nil {
			t.Fatal(err)
		}

		_, err := runGeneratePathForTest(t, generateOptions{
			path:   requestedSource,
			outDir: filepath.Dir(siblingOutput),
		})
		if err == nil {
			t.Fatal("partial generation accepted an irregular sibling output")
		}
		for _, want := range []string{
			requestedSource,
			siblingSource,
			siblingOutput,
			"cannot be verified",
			"not a regular file",
		} {
			if !strings.Contains(err.Error(), want) {
				t.Fatalf("error %q does not contain %q", err, want)
			}
		}
		if _, statErr := os.Stat(requestedOutput); !os.IsNotExist(statErr) {
			t.Fatalf("requested output exists after irregular failure: %v", statErr)
		}
	})
}

