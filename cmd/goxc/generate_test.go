func TestPartialPackageGenerationRejectsStaleSiblingAfterSourceAddition(t *testing.T) {
	root := newPackageIdentifierFixture(t)
	existingSource := filepath.Join(root, "view_A.gox")
	existingOutput := existingSource + ".go"
	requestedSource := filepath.Join(root, "view.gox")
	requestedOutput := requestedSource + ".go"
	writeTestFile(t, root, "view_A.gox", packageIdentifierGOXSource("Other", "B"))

	if _, err := runGeneratePathForTest(t, generateOptions{
		path:    existingSource,
		inPlace: true,
	}); err != nil {
		t.Fatalf("initial generatePath() error: %v", err)
	}
	existingBefore, err := os.ReadFile(existingOutput)
	if err != nil {
		t.Fatal(err)
	}
	initialIdentifier := generatedIdentifierForComponent(t, existingOutput, "B")

	writeTestFile(t, root, "view.gox", packageIdentifierGOXSource("View", "A_B"))
	_, generationErr := runGeneratePathForTest(t, generateOptions{
		path:    requestedSource,
		inPlace: true,
	})
	assertPartialGenerationStaleError(
		t,
		generationErr,
		requestedSource,
		existingSource,
		existingOutput,
		root,
	)
	if _, err := os.Stat(requestedOutput); !os.IsNotExist(err) {
		t.Fatalf("requested output exists after stale guard: %v", err)
	}
	assertFileBytes(t, existingOutput, existingBefore)

	if _, err := runGeneratePathForTest(t, generateOptions{path: root, inPlace: true}); err != nil {
		t.Fatalf("directory recovery error: %v", err)
	}
	if got := generatedIdentifierForComponent(t, existingOutput, "B"); got == initialIdentifier {
		t.Fatalf("directory recovery retained stale identifier %q", got)
	}
	if got := generatedIdentifierForComponent(t, requestedOutput, "A_B"); got == generatedIdentifierForComponent(t, existingOutput, "B") {
		t.Fatalf("directory recovery produced duplicate identifier %q", got)
	}
	goTestPackageIdentifierFixture(t, root)
}

